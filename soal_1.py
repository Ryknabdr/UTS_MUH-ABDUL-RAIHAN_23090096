bilangan_pertama = int(input("Masukkan bilangan pertama: "))
bilangan_kedua = int(input("Masukkan bilangan kedua: "))

penjumlahan = bilangan_pertama + bilangan_kedua
pengurangan = bilangan_pertama - bilangan_kedua
modulus = bilangan_pertama % bilangan_kedua

print(f"Hasil penjumlahan: {penjumlahan}")
print(f"Hasil pengurangan: {pengurangan}")
print(f"Hasil modulus: {modulus}")